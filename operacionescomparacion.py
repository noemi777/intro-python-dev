#Operadores comparacion
# ==, !=, <, >, <=, >=
edad = 18
edad2 = 15

iguales = edad == edad2
print(iguales)
diferente = edad != edad2
print(diferente)
menor = edad < edad2
print(menor)
mayor = edad > edad2
print(mayor)
menor_igual = edad <= edad2
print(menor_igual)
mayor_igual = edad >= edad2
print(mayor_igual)